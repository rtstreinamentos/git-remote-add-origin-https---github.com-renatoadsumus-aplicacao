CREATE TABLE Categoria (
	`Cd_Categoria` int(10) unsigned NOT NULL AUTO_INCREMENT,
	`Nm_Categoria` varchar(45) NOT NULL,
	`Cd_Categoria_Pai` int(10) unsigned NULL,
	`Ativo` tinyint(3) NOT NULL DEFAULT '0',
	`Url_Categoria` varchar(300) NOT NULL,
	`Url_Categoria_Title` varchar(300) NOT NULL,
  PRIMARY KEY (`Cd_Categoria`)
) ENGINE=InnoDB AUTO_INCREMENT=2 DEFAULT CHARSET=utf8;
